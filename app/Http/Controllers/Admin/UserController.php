    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $user = User::find($id);
        return view('admin.users.edit', compact('user'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    { 
        $user = User::find($id);



        if (isset($request->banning)) {
            $user->banned = 1;
        }else{
            $user->banned = 0;
        }

        if (isset($request->sellerStatus)) {
            $user->is_admin = 3;
        }else{
            $user->is_admin = 0;
        }

        $user->update($request->all());

        return redirect(route('users.index'))->with('flash_message', 'User status changed!');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        if ($user->is_admin) {
            return redirect()->route('users.index')->with('error', 'админа нельзя удалять');
        }
        $user->destroy($id);
        return redirect()->route('users.index')->with('flash_message', 'пользователь удален');
    }
